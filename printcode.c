void printchar(uint8_t characterindex,uint8_t x){
	set_bkg_data(x,1, pismenka[characterindex]); 	
}
void printmap(uint8_t x, uint8_t y, uint8_t layer, uint8_t delka,uint8_t tile){
	for(uint8_t woomilla = x; woomilla < x+delka; woomilla++){
		if(layer == 0) set_bkg_tile_xy(woomilla,y,tile);
		else set_win_tile_xy(woomilla,y,tile);
		tile++;
	}
}
void printtext(char text[15],uint8_t position){
uint8_t x = 0;
	for(uint8_t pismenko = 0; pismenko < 15; pismenko++){
		switch(text[pismenko]){
			case '0':
			case '1':
			case '2':
			case '3':
			case '4':
			case '5':
			case '6':
			case '7':
			case '8':
			case '9':
				printchar(text[pismenko] - '0',position+x);
				break;
			case ':':
				printchar(10,position+x);
				break;
			case '\0':
				pismenko = 15;
			default:
				printchar(11,position+x);
		}
		x++;
	}
}